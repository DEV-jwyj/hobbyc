package com.superman.www.dto;

public class UserDTO {

	private String id;
	private String pw;
	private String phone;
	private String postcode;
	private String read_address;
	private String code_address;
	private String detail_address;
	private String extra_address;
	private String join_day;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getRead_address() {
		return read_address;
	}
	public void setRead_address(String read_address) {
		this.read_address = read_address;
	}
	public String getCode_address() {
		return code_address;
	}
	public void setCode_address(String code_address) {
		this.code_address = code_address;
	}
	public String getDetail_address() {
		return detail_address;
	}
	public void setDetail_address(String detail_address) {
		this.detail_address = detail_address;
	}
	public String getExtra_address() {
		return extra_address;
	}
	public void setExtra_address(String extra_address) {
		this.extra_address = extra_address;
	}
	public String getJoin_day() {
		return join_day;
	}
	public void setJoin_day(String join_day) {
		this.join_day = join_day;
	}
	
	
}
